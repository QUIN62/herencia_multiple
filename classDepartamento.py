
class Departamento:
    def __init__(self):
        self._ubicacion = ""
        self._codigo = 0

    # Getters y Setters
    def get_ubicacion(self):
        return self._ubicacion

    def set_ubicacion (self, ubicacion):
        self._ubicacion = ubicacion

    def get_codigo(self):
        return self._codigo

    def set_codigo(self, codigo):
        self._codigo = codigo




